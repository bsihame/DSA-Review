const mulyt = ( arr ) => {
  let result = [];
  let product = arr.reduce((accum, n)=>   accum * n);
  
  for(let i = 0; i < arr.length; i++) {
    if(arr[i] === 0){
       result.push(0)
    }else {
    result.push(product/arr[i])
  }
  }
  return result

}
